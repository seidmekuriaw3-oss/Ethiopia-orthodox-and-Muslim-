def get_collection(collection_id: str):
    """Return a single collection dict or None."""
    return next((c for c in HADITH_COLLECTIONS if c["id"] == collection_id), None)


def get_collections_meta():
    """Return collection metadata (no hadiths list) for the index page."""
    return [
        {k: v for k, v in c.items() if k != "hadiths"}
        for c in HADITH_COLLECTIONS
    ]
